    private void ActualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ActualizarMouseClicked
        //Obtener el valor de CambioTXT
        String Cambio = CambioTXT.getText();

        //Validar si se ingres√≥ un valor.
        if (Cambio.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor para la actualizaci√≥n.", "Alerta", JOptionPane.WARNING_MESSAGE);
        } else {
            //Actualizar la informaci√≥n del cliente seg√∫n la opci√≥n seleccionada.
            //Verificar qu√© bot√≥n de modificaci√≥n est√° seleccionado.
             if (BotonNombre.isSelected()) {
                 //Actualizar el nombre del cliente con nuevoValor.
                 clienteActual.setNombre(Cambio);
             } else if (BotonPlaca.isSelected()) {
                 //Actualizar la placa del veh√≠culo con nuevoValor.
                 clienteActual.getVehiculos().get(0).setPlaca(Cambio);
             } else if (BotonTipo.isSelected()) {
                 //Actualizar el tipo de veh√≠culo con nuevoValor.
                 clienteActual.getVehiculos().get(0).setTipoVehiculo(Cambio);
             }

             //Luego de actualizar la informaci√≥n, guardarmos el cliente actualizado.
             ClientesCRUD.Update(clienteActual);

             JOptionPane.showMessageDialog(null, "Cliente actualizado correctamente.", "√âxito", JOptionPane.INFORMATION_MESSAGE);
        }
        
        this.setVisible(false);
        this.revalidate();
        this.repaint();
        
    }//GEN-LAST:event_ActualizarMouseClicked

